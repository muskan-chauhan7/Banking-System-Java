public class User {
    String name;
    String address;
    String phone;
    String password;
    int accountNumber;

    public User(String name, String address, String phone, String password, int accountNumber) {
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.password = password;
        this.accountNumber = accountNumber;
    }
}